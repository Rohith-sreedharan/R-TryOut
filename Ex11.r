# Create a vector
my_vector <- c(1, 2, 3, 4, 5)

# Create an array
my_array <- array(data = c(1:12), dim = c(3, 4))

# Create a matrix
my_matrix <- matrix(data = 1:9, nrow = 3, ncol = 3)

# Create a data frame
my_data_frame <- data.frame(ID = 1:3, Name = c("Alice", "Bob", "Charlie"), Age = c(25, 30, 35))

# Create a list
my_list <- list(numbers = c(1, 2, 3), fruits = c("apple", "banana", "orange"))

# show all in print
print(my_vector)
print(my_array)
print(my_matrix)
print(my_data_frame)
print(my_list)